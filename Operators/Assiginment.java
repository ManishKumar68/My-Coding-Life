 import java.util.*;
public class Assiginment {
    public static void main(String [] args) {
        // ( = ,  +=  , -= , *= , /=) equals Assignment Operator
        int A = 5;
        // A = A + 10;
        A += 10;
        System.out.println(A); // 15 (first print then increment)

        int B = 20;
        // B = B - 5;
        B -= 5;
        System.out.println(B); // 15 (Second print then increment)

        int C = 4;
        // C = C * 2;
        C *= 2;
        System.out.println(C);  // 8 (Third print then increment)
    

        int D = 30;
        // D = D / 3;
        D /= 3;
        System.out.println(D); // 10 (Fourth print then increment)
    }
}