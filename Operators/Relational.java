import java.util.*;
public class Relational {
    public static void main(String [] args) {
        int a = 10;
        int b = 20;
        System.out.println( "equals : " + (a == b));
        System.out.println( "not equals : " + (a != b));
        System.out.println("grater than: " +(a > b));
        System.out.println("lesser than: " +(a < b));
        System.out.println("grater than or equals  to : " + (a >= b));
        System.out.println("lesser than or equals to : " + (a <= b));

    }
}